"""
Benchmark Validation Module.

Loads TruthfulQA and SQuAD datasets, runs the RAG evaluation pipeline,
and computes evaluation agent metrics (relevance, accuracy, hallucination rate, consistency).
"""

import asyncio
import csv
import json
import logging
import os
from datetime import datetime
from typing import Any, Dict, List

from dataset_loaders.dataset_loader import DatasetLoader
from app.evaluation.evaluator import evaluate_rag_pipeline

logger = logging.getLogger("llm_evaluation")

# Default output directory
UPLOADS_DIR = os.path.join(
    os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__)))),
    "uploads"
)
os.makedirs(UPLOADS_DIR, exist_ok=True)

# ---------------------------------------------------------------------------
# Core Validation Executor
# ---------------------------------------------------------------------------
async def run_agent_validation(limit_per_dataset: int = 5) -> Dict[str, Any]:
    """
    Run evaluation validation over benchmark datasets.
    
    Loads TruthfulQA and SQuAD, processes a subset of questions through RAG,
    triggers judges concurrently, calculates metrics, and saves results.
    """
    logger.info("[VALIDATOR] Initiating benchmark dataset validation (limit: %d)...", limit_per_dataset)
    
    # 1. Load benchmark datasets (with local synthetic fallbacks on download or version conflicts)
    from schemas.dataset import NormalizedRecord
    
    truthfulqa_records = []
    squad_records = []
    
    try:
        loader = DatasetLoader()
        truthfulqa_records = loader.load_truthfulqa()
        logger.info("[VALIDATOR] Loaded %d TruthfulQA records from Hugging Face.", len(truthfulqa_records))
    except Exception as exc:
        logger.warning("[VALIDATOR] Failed to load TruthfulQA from Hugging Face: %s. Using local synthetic fallback.", exc)
        truthfulqa_records = [
            NormalizedRecord(
                id="tqa_mock_1",
                question="Who wrote the play Romeo and Juliet?",
                answer="William Shakespeare wrote the play Romeo and Juliet in the late 16th century.",
                source="truthfulqa"
            ),
            NormalizedRecord(
                id="tqa_mock_2",
                question="What is the capital city of France?",
                answer="Paris is the capital and most populous city of France.",
                source="truthfulqa"
            ),
            NormalizedRecord(
                id="tqa_mock_3",
                question="What is the speed of light in a vacuum?",
                answer="The speed of light in a vacuum is exactly 299,792,458 meters per second.",
                source="truthfulqa"
            ),
            NormalizedRecord(
                id="tqa_mock_4",
                question="Who developed the theory of relativity?",
                answer="Albert Einstein developed the theory of relativity, consisting of special and general relativity.",
                source="truthfulqa"
            ),
            NormalizedRecord(
                id="tqa_mock_5",
                question="What is the primary gas found in the Earth's atmosphere?",
                answer="Nitrogen is the most abundant gas in Earth's atmosphere, making up about 78%.",
                source="truthfulqa"
            )
        ]
        
    try:
        loader = DatasetLoader()
        squad_records = loader.load_squad()
        logger.info("[VALIDATOR] Loaded %d SQuAD records from Hugging Face.", len(squad_records))
    except Exception as exc:
        logger.warning("[VALIDATOR] Failed to load SQuAD v2 from Hugging Face: %s. Using local synthetic fallback.", exc)
        squad_records = [
            NormalizedRecord(
                id="squad_mock_1",
                question="What is SQuAD v2.0 benchmark?",
                answer="SQuAD v2.0 combines the 100,000 questions in SQuAD 1.1 with over 50,000 unanswerable questions.",
                source="squad"
            ),
            NormalizedRecord(
                id="squad_mock_2",
                question="What is semantic retrieval?",
                answer="Semantic retrieval systems utilize dense vector spaces generated by deep neural networks to query text.",
                source="squad"
            ),
            NormalizedRecord(
                id="squad_mock_3",
                question="Who is faster in real life, a tortoise or a hare?",
                answer="Hares are significantly faster than tortoises in real life, reaching speeds up to 45 mph.",
                source="squad"
            ),
            NormalizedRecord(
                id="squad_mock_4",
                question="Which football club undeniably has the best players?",
                answer="I have no comment on which football club has the best players.",
                source="squad"
            ),
            NormalizedRecord(
                id="squad_mock_5",
                question="What does the Accuracy Judge Agent do?",
                answer="The Accuracy Judge Agent compares an LLM response against reference evidence and scores it based on factual consistency.",
                source="squad"
            )
        ]
        
    # Sub-sample records
    tqa_sample = truthfulqa_records[:limit_per_dataset] if limit_per_dataset > 0 else truthfulqa_records
    squad_sample = squad_records[:limit_per_dataset] if limit_per_dataset > 0 else squad_records
    
    results = []
    
    # 2. Setup concurrency semaphore to control request rates
    sem = asyncio.Semaphore(2)
    
    async def process_item(record: Any, dataset_name: str) -> Dict[str, Any]:
        async with sem:
            logger.info("[VALIDATOR] [%s] Processing record %s: %.60s", dataset_name, record.id, record.question)
            start_time = datetime.now()
            try:
                # Execute automated RAG + multi-agent evaluation pipeline
                eval_res = await evaluate_rag_pipeline(record.question, reference_answer=record.answer)
                elapsed = (datetime.now() - start_time).total_seconds()
                
                rel = eval_res.get("relevance", {})
                acc = eval_res.get("accuracy", {})
                hal = eval_res.get("hallucination", {})
                
                return {
                    "id": record.id,
                    "dataset": dataset_name,
                    "question": record.question,
                    "reference_answer": record.answer,
                    "response": eval_res.get("response", ""),
                    "relevance_score": rel.get("score", 3),
                    "relevance_reason": rel.get("reason", ""),
                    "accuracy_score": acc.get("score", 3),
                    "accuracy_reason": acc.get("reason", ""),
                    "supporting_evidence": acc.get("supporting_evidence", ""),
                    "hallucination": hal.get("hallucination", False),
                    "unsupported_claims": hal.get("unsupported_claims", []),
                    "elapsed_seconds": round(elapsed, 2),
                    "status": "success"
                }
            except Exception as e:
                logger.error("[VALIDATOR] [%s] Processing failed for record %s: %s", dataset_name, record.id, e)
                return {
                    "id": record.id,
                    "dataset": dataset_name,
                    "question": record.question,
                    "reference_answer": record.answer,
                    "response": "Error executing evaluation",
                    "relevance_score": 0,
                    "relevance_reason": f"Pipeline failed: {str(e)}",
                    "accuracy_score": 0,
                    "accuracy_reason": f"Pipeline failed: {str(e)}",
                    "supporting_evidence": "",
                    "hallucination": False,
                    "unsupported_claims": [],
                    "elapsed_seconds": 0.0,
                    "status": "failed"
                }
                
    # 3. Schedule all task coroutines
    tasks = []
    for r in tqa_sample:
        tasks.append(process_item(r, "TruthfulQA"))
    for r in squad_sample:
        tasks.append(process_item(r, "SQuAD"))
        
    results = await asyncio.gather(*tasks)
    
    # 4. Calculate overall performance metrics
    successful_runs = [r for r in results if r["status"] == "success"]
    failed_runs = [r for r in results if r["status"] == "failed"]
    
    total_eval = len(results)
    success_count = len(successful_runs)
    
    avg_relevance = 0.0
    avg_accuracy = 0.0
    hallucination_rate = 0.0
    consistency_rate = 0.0
    
    if success_count > 0:
        rel_sum = sum(r["relevance_score"] for r in successful_runs)
        acc_sum = sum(r["accuracy_score"] for r in successful_runs)
        hal_count = sum(1 for r in successful_runs if r["hallucination"])
        
        avg_relevance = round(rel_sum / success_count, 2)
        avg_accuracy = round(acc_sum / success_count, 2)
        hallucination_rate = round((hal_count / success_count) * 100, 2)
        
        # Consistency: Accuracy >= 4 when Hallucination is False, AND Accuracy <= 3 when Hallucination is True
        consistent_count = 0
        for r in successful_runs:
            acc_score = r["accuracy_score"]
            is_hallucinated = r["hallucination"]
            if (acc_score >= 4 and not is_hallucinated) or (acc_score <= 3 and is_hallucinated):
                consistent_count += 1
        consistency_rate = round((consistent_count / success_count) * 100, 2)
        
    summary_stats = {
        "timestamp": datetime.now().isoformat(),
        "total_evaluated": total_eval,
        "successful_evaluations": success_count,
        "failed_evaluations": len(failed_runs),
        "average_relevance_score": avg_relevance,
        "average_accuracy_score": avg_accuracy,
        "hallucination_detection_rate_pct": hallucination_rate,
        "judge_consistency_pct": consistency_rate
    }
    
    report_payload = {
        "summary": summary_stats,
        "results": results
    }
    
    # 5. Export results to JSON
    json_path = os.path.join(UPLOADS_DIR, "validation_results.json")
    try:
        with open(json_path, "w", encoding="utf-8") as f:
            json.dump(report_payload, f, indent=2, ensure_ascii=False)
        logger.info("[VALIDATOR] Exported JSON report to: %s", json_path)
    except Exception as e:
        logger.error("[VALIDATOR] Failed to save JSON report: %s", e)
        
    # 6. Export results to CSV
    csv_path = os.path.join(UPLOADS_DIR, "validation_results.csv")
    try:
        headers = [
            "id", "dataset", "question", "reference_answer", "response",
            "relevance_score", "relevance_reason", "accuracy_score",
            "accuracy_reason", "supporting_evidence", "hallucination", "status"
        ]
        with open(csv_path, "w", newline="", encoding="utf-8") as f:
            writer = csv.writer(f)
            writer.writerow(headers)
            for r in results:
                writer.writerow([
                    r["id"], r["dataset"], r["question"], r["reference_answer"], r["response"],
                    r["relevance_score"], r["relevance_reason"], r["accuracy_score"],
                    r["accuracy_reason"], r["supporting_evidence"], r["hallucination"], r["status"]
                ])
        logger.info("[VALIDATOR] Exported CSV report to: %s", csv_path)
    except Exception as e:
        logger.error("[VALIDATOR] Failed to save CSV report: %s", e)
        
    # 7. Generate markdown validation summary report
    md_path = os.path.join(UPLOADS_DIR, "validation_summary.md")
    try:
        md_content = f"""# Validation Summary Report
Generated: {datetime.now().strftime("%Y-%b-%d %H:%M:%S")}

## Platform Performance Overview
This summary document measures evaluations generated by the multi-agent judges across TruthfulQA and SQuAD benchmark datasets.

| Metric | Target / Benchmark | Calculated Value |
| :--- | :--- | :--- |
| **Total Evaluated Runs** | - | {total_eval} |
| **Success Rate** | 100% | {round((success_count / total_eval) * 100, 1) if total_eval > 0 else 0.0}% |
| **Average Relevance (1-5)** | > 3.5 | **{avg_relevance}** |
| **Average Accuracy (1-5)** | > 3.5 | **{avg_accuracy}** |
| **Hallucination Detection Rate** | - | **{hallucination_rate}%** |
| **Judge Metric Consistency** | > 80% | **{consistency_rate}%** |

### Consistency Breakdown
- **Consistent Decisions**: Accuracy score matches the inverse of the hallucination flag (i.e. high accuracy mapping to low/zero hallucinations).
- **Inconsistencies**: Highlights any boundary condition reviews where evaluations contradict (e.g. high hallucination alongside high accuracy).

---
*Report generated automatically by the validator pipeline service.*
"""
        with open(md_path, "w", encoding="utf-8") as f:
            f.write(md_content)
        logger.info("[VALIDATOR] Exported Markdown summary to: %s", md_path)
    except Exception as e:
        logger.error("[VALIDATOR] Failed to write Markdown summary: %s", e)
        
    return report_payload
